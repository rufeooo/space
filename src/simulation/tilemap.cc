void
TilemapEnableFog()
{
  for (int i = 0; i < kMapHeight; ++i) {
    for (int j = 0; j < kMapWidth; ++j) {
      Tile* tile = TilePtr(v2i(j, i));
      tile->can_shroud = !tile->exterior;
    }
  }
}

void
TilemapResetExterior()
{
  for (int i = 0; i < kUsedShip; ++i) {
    TilemapModify tm(i);
    Tile set_tile = kZeroTile;
    set_tile.cx = 0;
    set_tile.cy = 0;
    set_tile.exterior = 1;
    BfsTileEnable(set_tile);
  }
}

