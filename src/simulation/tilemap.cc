// Integer math to measure the distance squred to the center of a tile
bool
TileDsq(int64_t dx, int64_t dy, uint64_t tile_distance)
{
  const uint64_t dt = (tile_distance * 2) + 2;
  const uint64_t dsq = (dt * dt);
  uint64_t dx2 = ABS64(dx) * 2 + 1;
  uint64_t dy2 = ABS64(dy) * 2 + 1;
  return (dx2 * dx2 + dy2 * dy2 <= dsq);
}

#define INVALID_TILE v2i(0, 0)

constexpr int kMaxNeighbor = 8;
constexpr int kLastNeighbor = kMaxNeighbor - 1;
static const v2i kNeighbor[kMaxNeighbor] = {
    v2i(-1, 0), v2i(1, 0),  v2i(0, 1),  v2i(0, -1),
    v2i(1, 1),  v2i(-1, 1), v2i(1, -1), v2i(-1, -1),
};

// Returns the minimum position of the tile.
v2f
TilePosToWorldMin(const v2i pos)
{
  return {kTilemapWorldOffset.x + ((float)pos.x * kTileWidth),
          kTilemapWorldOffset.y + ((float)pos.y * kTileHeight)};
}

// Returns the centered position of the tile.
v2f
TilePosToWorld(const v2i pos)
{
  return TilePosToWorldMin(pos) + v2f(kTileWidth * .5f, kTileHeight * .5f);
}

// Returns the minimum position of the tile.
v2f
TileToWorld(const Tile tile)
{
  return TilePosToWorld(v2i(tile.cx, tile.cy));
}

// Returns true for positions that exist as a tile in kTilemap
bool
TileOk(v2i pos)
{
  if (pos.x < 0) return false;
  if (pos.x >= kMapWidth) return false;
  if (pos.y < 0) return false;
  if (pos.y >= kMapHeight) return false;
  return true;
}

Tile*
TilePtr(const v2i& pos)
{
  if (!kCurrentGrid) return nullptr;
  if (!TileOk(pos)) return nullptr;
  return &kCurrentGrid->tilemap[pos.y][pos.x];
}

v3f
TileAvoidWalls(Tile tile)
{
  v2i avoidance = {};
  for (int i = 0; i < kMaxNeighbor; ++i) {
    v2i neighbor = v2i(tile.cx, tile.cy) + kNeighbor[i];
    Tile* tile = TilePtr(neighbor);
    if (!tile) continue;
    if (tile->blocked) {
      avoidance -= kNeighbor[i];
    }
  }

  return v3f(avoidance.x, avoidance.y, 0.0f);
}

v2f
TileRandomPosition()
{
  v2i random_tile(MOD_BUCKET(rand(), kMapWidth),
                  MOD_BUCKET(rand(), kMapHeight));
  return TilePosToWorld(random_tile);
}

Tile
TileNeighbor(Tile tile, uint64_t index)
{
  v2i n = kNeighbor[MOD_BUCKET(index, kMaxNeighbor)];

  tile.cx = BITRANGE_WRAP(tile.xy_bitrange, tile.cx+n.x);
  tile.cy = BITRANGE_WRAP(tile.xy_bitrange, tile.cy+n.y);

  return tile;
}

Rectf
TilemapWorldBounds()
{
  Rectf ret;
  v2f minf = TilePosToWorldMin({0, 0});
  v2f maxf = TilePosToWorldMin({kMapWidth, kMapHeight});

  ret.x = minf.x;
  ret.y = minf.y;
  ret.width = maxf.x - minf.x;
  ret.height = maxf.y - minf.y;

  return ret;
}

v3f
TilemapWorldCenter()
{
  return TilePosToWorld({0, 0}) +
         v2f(kMapWidth * kTileWidth * .5f, kMapHeight * kTileHeight * .5f);
}

void
TilemapClear()
{
  kCurrentGrid = nullptr;
  kTilemapWorldOffset = v2f();
}

class TilemapModify
{
 public:
  TilemapModify(uint64_t ship_index)
  {
    prev_grid = kCurrentGrid;
    prev_offset = kTilemapWorldOffset;
    // TODO (AN): For now grid_index is ship_index
    ok = ship_index < kUsedGrid && ship_index < kUsedShip;

    if (!ok) return;

    // TODO (AN): For now grid_index is ship_index
    kCurrentGrid = &kGrid[ship_index];
    kTilemapWorldOffset = kShip[ship_index].transform.position.xy();
  }

  ~TilemapModify()
  {
    kCurrentGrid = prev_grid;
    kTilemapWorldOffset = prev_offset;
  }

  Grid* prev_grid;
  v2f prev_offset;
  bool ok;
};

int64_t
TilemapWorldToGrid(v3f world)
{
  for (int i = 0; i < kUsedShip; ++i) {
    TilemapModify tm(i);
    if (!tm.ok) continue;
    Rectf r(kTilemapWorldOffset.x, kTilemapWorldOffset.y,
                  kMapWidth * kTileWidth, kMapHeight * kTileHeight);
    if (math::PointInRect(world.xy(), r)) {
      return i;
    }
  }
  return kInvalidIndex;
}

bool
WorldToTile(const v3f pos, Tile* t)
{
  *t = kZeroTile;
  int64_t tidx = TilemapWorldToGrid(pos);
  if (tidx == kInvalidIndex) return false;

  TilemapModify tm(tidx);
  v2f relpos = pos.xy() - kTilemapWorldOffset;
  t->cx = relpos.x/kTileWidth;
  t->cy = relpos.y/kTileHeight;
  t->flags = 0;
  return true;
}

bool
WorldToTilePos(const v3f pos, v2i* t)
{
  int64_t tidx = TilemapWorldToGrid(pos);
  if (tidx == kInvalidIndex) return false;

  TilemapModify tm(tidx);
  v2f relpos = pos.xy() - kTilemapWorldOffset;
  *t = v2i((int)(relpos.x / kTileWidth), (int)(relpos.y / kTileHeight));
  return true;
}

