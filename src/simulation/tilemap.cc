  switch (type) {
    case kTilemapEmpty: {
      for (int y = 0; y < kMapHeight; ++y) {
        for (int x = 0; x < kMapWidth; ++x) {
          Tile* tile = TilePtr(v2i(x, y));
          *tile = kZeroTile;
          tile->cx = x;
          tile->cy = y;
        }
      }
    } break;
    case kTilemapShip: {
      for (int y = 0; y < kMapHeight; ++y) {
        for (int x = 0; x < kMapWidth; ++x) {
          Tile* tile = TilePtr(v2i(x, y));
          *tile = kZeroTile;
          tile->cx = x;
          tile->cy = y;
          tile->blocked = (kDefaultMap[y][x] == kTileBlock);
          tile->nooxygen = 0;

          // Consumables enabled: cryo chamber, gatherable resources
          if (kDefaultMap[y][x] == kTileConsumable) {
            Consumable* c = UseConsumable();
            c->ship_index = ship_index;
            c->cx = tile->cx;
            c->cy = tile->cy;
            c->minerals = y * x % 89;
            if (kDefaultMap[y][x - 1] == kTileBlock) {
              c->cryo_chamber = 1;
            }
          }

          switch (kDefaultMap[y][x]) {
            case kTilePower:
            case kTileEngine:
            case kTileMine:
            case kTileDoor:
            case kTileTurret: {
              Module* mod = UseEntityModule();
              mod->mkind = (ModuleKind)(kDefaultMap[y][x] - kTileModule);
              mod->bounds = ModuleBounds(mod->mkind);
              mod->ship_index = ship_index;
              mod->player_index = player_index;
              v3f mp = TilePosToWorld(v2i(x, y));
              mod->position = mp + v3f(0.f, 0.f, mod->bounds.z / 2.f);
            } break;
          };
        }
      }
    } break;
  }
